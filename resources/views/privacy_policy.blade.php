            <h3><strong><em><u>7. ინტელექტუალური საკუთრება</u></em></strong></h3>
            <p><strong>7.1.</strong> ვებგვერდზე განთავსებული ყველა შინაარსი (ტექსტი, დიზაინი, ლოგოები, გრაფიკა და ა.შ.) წარმოადგენს ინსტიტუტის (GIPC) საკუთრებას ან ლიცენზირებულია მის მიერ და დაცულია საავტორო უფლებებით. <strong>7.2.</strong> მომხმარებელს ეძლევა შეზღუდული, პერსონალური გამოყენების ლიცენზია წინამდებარე წესების შესაბამისად.</p>
            <h3><strong><em><u>8. პასუხისმგებლობის შეზღუდვა (დისკლეიმერი)</u></em></strong></h3>
            <p><strong>8.1.</strong> ინსტიტუტი არ იძლევა გარანტიას ვებგვერდზე განთავსებული ინფორმაციის აბსოლუტურ სიზუსტესა და მუდმივ ხელმისაწვდომობაზე. <strong>8.2.</strong> ინსტიტუტი არ არის პასუხისმგებელი მომხმარებლების მიერ გენერირებული კონტენტის, მესამე მხარეების ბმულების შინაარსის, ვებგვერდის გამოყენებით მიღებული შედეგების ან მიყენებული ზიანისთვის (გარდა კანონით პირდაპირ გათვალისწინებული შემთხვევებისა). <strong>8.3.</strong> ვებგვერდი და სერვისები მოწოდებულია &bdquo;როგორც არის&ldquo; პრინციპით.</p>
