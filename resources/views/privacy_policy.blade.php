            <h3><strong><em><u>9. კონფიდენციალურობის პოლიტიკა</u></em></strong></h3>
            <p><strong>9.1. მომხმარებლის მონაცემების შეგროვება:</strong></p>
            <ul>
            <li><strong>პერსონალური მონაცემები</strong>: სახელი, გვარი, ელფოსტა, ტელეფონის ნომერი, საიდენტიფიკაციო ნომერი (საჭიროებისამებრ).</li>
            <li><strong>გადახდის მონაცემები</strong>: გადახდის მეთოდის დეტალები (მაგ., ბარათის ბოლო 4 ციფრი), შეკვეთის ID, თანხა.</li>
            <li><strong>ტექნიკური მონაცემები</strong>: IP მისამართი, ბრაუზერის ტიპი, ვებგვერდის გამოყენების სტატისტიკა.</li>
            </ul>
            <p><strong>9.2. მონაცემთა შეგროვებისა და გამოყენების მიზანი:</strong></p>
            <p>მონაცემები მუშავდება:</p>
            <ul>
            <li>სერვისის მიწოდებისთვის (სერტიფიცირება, ტრენინგი, გადახდის დამუშავება).</li>
            <li>მომხმარებელთან კომუნიკაციისთვის (მოთხოვნების განხილვა, სიახლეების გაგზავნა თანხმობის საფუძველზე).</li>
            <li>სერვისის ხარისხის გასაუმჯობესებლად (ანალიზი, ოპტიმიზაცია).</li>
            </ul>
            <p><strong>9.3. მონაცემთა დაცვა:</strong></p>
            <p>მონაცემთა უსაფრთხოების უზრუნველსაყოფად:</p>
            <ul>
            <li>გამოიყენება SSL/TLS დაშიფვრა.</li>
            <li>წვდომა შეზღუდულია ავტორიზებული პერსონალით.</li>
            <li>გადახდის პროცესი შეესაბამება PCI DSS სტანდარტებს.</li>
            <li>მონაცემთა დარღვევის შემთხვევაში, აღნიშნულის შესახებ მომხმარებლებს ეცნობათ დარღვევის აღმოჩენიდან 72 საათის განმავლობაში.</li>
            <li>მონაცემები ინახება 3 წლის განმავლობაში, მოქმედი კანონმდებლობის შესაბამისად.</li>
            </ul>
            <p><strong>9.4. მონაცემთა გაზიარება:</strong></p>
            <p>მონაცემები გადაეცემა:</p>
            <ul>
            <li>გადახდის პროვაიდერს (Flitt) მხოლოდ გადახდის დასამუშავებლად, PCI DSS-ის შესაბამისად.</li>
            <li>სამართლებრივი მოთხოვნის საფუძველზე, კანონით განსაზღვრულ შემთხვევებში.</li>
            </ul>
            <p><strong>9.5. მომხმარებლის უფლებები:</strong></p>
            <p>მომხმარებელს უფლება აქვს:</p>
            <ul>
            <li>მოითხოვოს მონაცემთა წვდომა, გასწორება, წაშლა ან დამუშავების შეზღუდვა.</li>
            <li>გააპროტესტოს მონაცემთა გამოყენება.</li>
            <li>კონფიდენციალურობასთან დაკავშირებულ შეკითხვებზე დაუკავშირდეს კომპანიას.</li>
            </ul>
            <p><strong>9.6. ცვლილებები პოლიტიკაში:</strong></p>
            <ul>
            <li>კონფიდენციალურობის პოლიტიკის შესახებ ნებისმიერი ცვლილება გამოქვეყნდება ვებგვერდზე (<a href="https://gipc.ge">https://gipc.ge</a>).</li>
            </ul>
            <h3><strong><em><u>10. წვდომის შეწყვეტა ან შეჩერება</u></em></strong></h3>
            <p>ინსტიტუტი იტოვებს უფლებას, შეუჩეროს ან შეუწყვიტოს მომხმარებელს ვებგვერდზე წვდომა წინამდებარე წესების დარღვევის შემთხვევაში.</p>
            <h3><strong><em><u>11. ცვლილებები წესებში</u></em></strong></h3>
            <p>ინსტიტუტს აქვს უფლება, ნებისმიერ დროს განაახლოს ეს წესები და პირობები. ცვლილებები ძალაში შევა მათი ვებგვერდზე გამოქვეყნებისთანავე. ვებგვერდის გამოყენების გაგრძელება ნიშნავს განახლებული წესების მიღებას.</p>
            <h3><strong><em><u>12. მარეგულირებელი კანონმდებლობა და დავების გადაწყვეტა</u></em></strong></h3>
            <p>წესები რეგულირდება საქართველოს კანონმდებლობით. დავები გადაწყდება მოლაპარაკების გზით, ხოლო შეთანხმების მიუღწევლობის შემთხვევაში &ndash; საქართველოს სასამართლოს მიერ.</p>
            <h3><strong><em><u>13. საკონტაქტო ინფორმაცია</u></em></strong></h3>
            <p>წესებთან დაკავშირებული კითხვების შემთხვევაში, გთხოვთ, დაგვიკავშირდეთ ვებგვერდზე მითითებული საკონტაქტო ინფორმაციის საშუალებით.</p>
